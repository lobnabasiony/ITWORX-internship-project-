                                <h3 class="header-of-story">Executive Summary</h3>
                               <p class="paragraph of story">When Zain-Iraq recently replaced their Business Support Systems (BSS) with new Huawei solutions, they needed complete end-to-end User Acceptance Testing (UAT) of the customer care and billing system (Huawei NGBSS).
                                    Some work had already been done, and time was now of the essence. The Ericsson project team therefore, looked for a highly professional partner, experienced in the Telecoms industry, to perform the testing, and to lead the handover to various business departments.
                                    ITWORX was selected. We not only achieved the timeline, we also delivered a smooth transition and a superior testing service – one that got business users involved and familiarized with the new systems. The result fully met – and even exceeded – Ericsson and Zain-Iraq’s expectations.
                                </p> 
                                <div class="list-title"></div>
                                <ul class="story-list ">
                                </ul>
                               

                                <br>
                                <h3 class="header-of-story">The Challenge</h3>
                                <p class="paragraph of story">As well as a time critical process, Zain-Iraq also wanted to avoid any interruption or dysfunction in the normal operations of all business departments during the testing and transition to the new Huawei-based system.
                                     </p> 
                                     <div class="list-title">This was particularly important as the new BSS included numerous modules, such as:</div>
                                 <ul class="story-list ">
                                   <li  >    Customer Relation Management (CRM)</li>
                                   <li  >        Convergent Billing Solution (CBS)</li>
                                   <li  >    Electronic Voucher Center (EVC) and Universal Voucher Center (UVC)</li>   
                                   <li  >    IP Contact Center (IPCC)</li>   
                                   <li  >     Mediation</li> 
                                   <li  >     Provisioning</li>  
                                 </ul>


                                 <br>
                                 <h3 class="header-of-story">The Solution</h3>
                                 <p class="paragraph of story">Based on our solid experience in testing different Telecoms systems, the ITWORX team reviewed all the requirements and test cases. We also created new test cases, not included in the initial test book, in order to thoroughly investigate all possible scenarios.

                                        We provided an offshore team of Telecoms professionals. This team carried out all the test cases, including the back-end scenarios, and only required a minimal level of onsite support. In addition, we ensured direct communication with Huawei and Ericsson on a day-to-day basis.
                                        
                                        Formal reviews were set in place for each activity the testing team performed, to ensure it was implemented as per the plan, and met the requirements specified in the FRS.
                                      </p> 
                                      <div class="list-title">The scope of testing included:</div>
                                  <ul class="story-list ">
                                    <li>  Test planning                                             </li>    
                                    <li>     Detailed timeline for each activity in the test cycle      </li>
                                    <li>     Test preparation                                       </li>
                                    <li>      Test case design                                           </li>               
                                    <li>    Test case preparation                                             </li>  
                                    <li>     Test data preparation                                              </li>
                                    <li>     Test execution                                             </li>
                                    <li>     Regression testing                                             </li>
                                    <li>       Integration testing                                          </li>
                                    <li>       System testing                                               </li>
                                    <li>         Co-ordination with the onsite team                              </li>               
                                    <li>         Defect management and reporting                                      </li>      
                                    <li>          Status reporting                                                      </li>
                                    <li>          Daily and weekly status updates                                       </li>
                                  </ul>





                                  <br>
                                  <h3 class="header-of-story">The Benefits</h3>
                                  <p class="paragraph of story">Our solution delivered a smooth transformation from the legacy BSS to the new one, without causing any dysfunction.

                                        This was a superior testing service, which achieved the business objectives using an optimal technical approach without making any risky trade-offs.
                                        
                                        The fact our team consisted of highly experienced Telecoms professionals ensured all possible end-to-end scenarios could be covered.
